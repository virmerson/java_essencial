package calc;

public class BitWise {

	public static void main(String[] args) {
		
		int a =60;
		int b= 13;
		
		int c =  a&b;
		
		
		System.out.println("60 = "+  Integer.toBinaryString(a));
		System.out.println("13 = "+  Integer.toBinaryString(b));
		System.out.println("12 = " + Integer.toBinaryString(c));
		
	}

}
